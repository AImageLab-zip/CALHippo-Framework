DEFAULT_GT_DIR: Path = Path(
    "data/output/test_lr_density_gt/test_set_gt_allCA_128_96_smooth_b05_k5_roi"
)
DEFAULT_GT_OUTPUT_DIR: Path = Path(
    "data/output/lr_gt_eval/allCA_best_model_128_96_smooth_b05_k5_roi"
)


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(
        description=(
            "Predict density maps on low-resolution WSIs and "
            "evaluate them against GT density maps."
        ),
    )
    parser.add_argument(
        "--input-dir",
        type=Path,
        default=DEFAULT_INPUT_DIR,
        help=(
            "Directory containing the low-res WSI PNG images. TODO: validate "
            "that using the full LR crop folder remains the right default once "
            "the GT preparation script is implemented."
        ),
    )
    parser.add_argument(
        "--gt-dir",
        type=Path,
        default=DEFAULT_GT_DIR,
        help=(
            "Directory containing {wsi_id}_original_density_aligned.npy "
            "GT files. TODO: generated by a future test LR GT preparation script."
        ),
    )
    parser.add_argument(
        "--output-dir",
        type=Path,
        default=DEFAULT_GT_OUTPUT_DIR,
        help="Directory where predictions and metrics will be saved.",
    )
    parser.add_argument(
        "--model-path",
        type=Path,
        default=DEFAULT_MODEL_PATH,
        help=(
            "Path to the experiment run folder (contains YAML config and .pth weights)."
        ),
    )
    parser.add_argument(
        "--roi-class",
        type=str,
        default=DEFAULT_CA_NAME,
        help="GeoJSON classification name that identifies ROI polygons.",
    )
    parser.add_argument(
        "--ca-to-map",
        nargs="+",
        type=str,
        default=DEFAULT_CA_LIST,
        help=(
            "List of CA class labels to extract from the GeoJSON "
            "(e.g. RCA1 RCA2 RCA3 RCA4)."
        ),
    )
    parser.add_argument(
        "--save-visuals",
        action="store_true",
        default=DEFAULT_SAVE_VISUALS,
        help="Enable saving visualisation PNGs (enabled by default).",
    )
    parser.add_argument(
        "--debug",
        action="store_true",
        help="Enable DEBUG-level logging.",
    )
    return parser.parse_args()


def filter_and_attach_gt_density_paths(
    wsis_data: list[dict[str, Path | None]],
    gt_dir: Path,
) -> list[dict[str, Path | None]]:
    """
    Filter discovered WSI entries to keep only those with a matching GT density file,
    and attach GT density file paths to the WSI entries.
    """

    wsis_with_gt: list[dict[str, Path | None]] = []

    for wsi_entry in wsis_data:
        wsi_path = wsi_entry["wsi_path"]
        if wsi_path is None:
            raise ValueError("wsi_entry['wsi_path'] must not be None")

        wsi_id = wsi_path.stem.split(sep="_")[0]
        gt_density_path = gt_dir / f"{wsi_id}_original_density_aligned.npy"

        if gt_density_path.exists():
            # Update the dict with the GT density path and add to the list
            wsis_with_gt.append({**wsi_entry, "gt_density_path": gt_density_path})

    logger.info(
        f"Found {len(wsis_with_gt)} WSI entries with matching GT density files."
    )

    return wsis_with_gt


def predict_and_evaluate_on_gt() -> None:
    cli = parse_args()
    setup_logging(debug=cli.debug)

    roi_class = cli.roi_class
    ca_list = cli.ca_to_map
    lr_wsi_dir = cli.input_dir
    gt_dir = cli.gt_dir
    output_dir = cli.output_dir
    model_folder_path = cli.model_path
    save_visualizations = cli.save_visuals

    (
        model,
        val_transform,
        effective_class_list,
        channel_to_predict,
        max_pix_value,
        patch_size,
        stride,
        num_classes,
        device,
    ) = load_model_and_val_transform(run_folder_path=model_folder_path)

    wsis_data, save_visualizations = discover_wsi_data(
        lr_wsi_dir=lr_wsi_dir,
        output_dir=output_dir,
        debug=cli.debug,
        save_visualizations=save_visualizations,
    )
    wsis_data = filter_and_attach_gt_density_paths(wsis_data=wsis_data, gt_dir=gt_dir)

    metriccs_dict: dict = {}

    for wsi_entry in wsis_data:
        prediction_outputs = run_prediction_for_wsi(
            wsi_entry=wsi_entry,
            model=model,
            val_transform=val_transform,
            max_pix_value=max_pix_value,
            patch_size=patch_size,
            stride=stride,
            num_classes=num_classes,
            device=device,
            roi_class=roi_class,
            ca_list=ca_list,
            effective_class_list=effective_class_list,
            save_visualizations=save_visualizations,
        )

        metrics = compute_density_metrics(
            gt_density_mask_path=wsi_entry["gt_density_path"],
            pred_density=prediction_outputs["pred_array"],
            roi_mask=prediction_outputs["roi_mask"],
            class_list=effective_class_list,
            channel_to_predict=channel_to_predict,
        )

        if metrics:
            metriccs_dict[prediction_outputs["wsi_id"]] = metrics

    logger.info("Processing complete.")
    aggregate_and_save_metrics(metriccs_dict, output_dir=output_dir)
    logger.info("Pipeline finished.")


def main() -> None:
    predict_and_evaluate_on_gt()


if __name__ == "__main__":
    main()
